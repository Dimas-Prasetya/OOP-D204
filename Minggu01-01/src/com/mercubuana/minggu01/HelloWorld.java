package com.mercubuana.minggu01;

public class HelloWorld {

	public static void main(String[] args) {
		
		// Menampilkan string dan pindah baris
		System.out.println("Halo Mahasiswa UMB");
		// Menampilkan string tanpa pindah baris
		System.out.print("Nama ");
		System.out.print("saya ");
		System.out.print("Dimas\n"); // \n adalah karakter pindah baris baru
		System.out.println("Ini baris baru lagi");
		// Menggunakan Ctrl + Enter untuk menyambung string
		System.out.println("Nama saya adalah Dimas dan saya adalah mahasiswa di"
				+ "mahasiswa di Fakultas Ilmu Komputer Universitas Mercu Buana"
				+ "sejak tahun 2015");
	
	}

}
